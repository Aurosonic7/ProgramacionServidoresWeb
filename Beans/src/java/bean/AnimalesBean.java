package bean;

public class AnimalesBean {
    private String nombreAnimal;
    private String colorAnimal;
    private String razaAnimal;
    private Integer edadAnimal;
    private Double altura;

    /**
     * @return the nombreAnimal
     */
    public String getNombreAnimal() {
        return nombreAnimal;
    }

    /**
     * @param nombreAnimal the nombreAnimal to set
     */
    public void setNombreAnimal(String nombreAnimal) {
        this.nombreAnimal = nombreAnimal;
    }

    /**
     * @return the colorAnimal
     */
    public String getColorAnimal() {
        return colorAnimal;
    }

    /**
     * @param colorAnimal the colorAnimal to set
     */
    public void setColorAnimal(String colorAnimal) {
        this.colorAnimal = colorAnimal;
    }

    /**
     * @return the razaAnimal
     */
    public String getRazaAnimal() {
        return razaAnimal;
    }

    /**
     * @param razaAnimal the razaAnimal to set
     */
    public void setRazaAnimal(String razaAnimal) {
        this.razaAnimal = razaAnimal;
    }

    /**
     * @return the edadAnimal
     */
    public Integer getEdadAnimal() {
        return edadAnimal;
    }

    /**
     * @param edadAnimal the edadAnimal to set
     */
    public void setEdadAnimal(Integer edadAnimal) {
        this.edadAnimal = edadAnimal;
    }

    /**
     * @return the altura
     */
    public Double getAltura() {
        return altura;
    }

    /**
     * @param altura the altura to set
     */
    public void setAltura(Double altura) {
        this.altura = altura;
    }
    
    
    
}
